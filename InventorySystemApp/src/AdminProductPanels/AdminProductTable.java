    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Admin admin = Admin.getInstance();
        ArrayList<Product> products = admin.getAllProducts();
        int index = jTable1.getSelectedRow();

        if (index != -1) {

            Product product = products.get(index);
            product.setName(nameField.getText()); 
            product.setPurchasingPrice(Double.parseDouble(purchaseField.getText()));
            product.setSellingPrice(Double.parseDouble(sellPrice.getText()));
            product.setCategory(Category.valueOf(categoryBox.getSelectedItem().toString()));
            product.setExpirationDate(expirationField.getValue().toString());
            product.setProductionDate(productionField.getValue().toString());
            admin.updateProduct(product, index);
            resetFields();
            jTable1.getSelectionModel().clearSelection();
            updateData();
        }
    }//GEN-LAST:event_updateButtonActionPerformed


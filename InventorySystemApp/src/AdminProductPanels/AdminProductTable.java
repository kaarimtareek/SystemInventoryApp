    private void resetFields() {
        nameField.setText("");
        idField.setText("");
        purchaseField.setText("");
        sellPrice.setText("");
        categoryBox.setSelectedIndex(0);

    }

    private void setDataToFields(Product product) {
        nameField.setText(product.getName());
        idField.setText("" + product.getId());
        purchaseField.setText("" + product.getPurchasingPrice());
        sellPrice.setText("" + product.getSellingPrice());
        ComboBoxModel model = categoryBox.getModel();
        model.setSelectedItem(product.getCategoryString());

    }


    private void addDataToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rawData[] = new Object[8];
        Admin admin = Admin.getInstance();
        ArrayList<Product> products = admin.getAllProducts();
        for (Product product : products) {
            rawData[0] = product.getName();
            rawData[1] = product.getId();
            rawData[2] = product.getCategoryString();
            rawData[3] = product.getProductionDate();
            rawData[4] = product.getExpirationDate();
            rawData[5] = product.getPurchasingPrice();
            rawData[6] = product.getSellingPrice();
            rawData[7] = product.getAvailableQuantity();
            model.addRow(rawData);
        }

    }

